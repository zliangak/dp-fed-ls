def shakespeare_noniid(dataset):
    """
    Sample Non-I.I.D. client data from Shakespeare dataset
    :param dataset: the training set
    :return: dict of image index
    """
    dict_users = {}
    client_ids = list(dataset.dict_users.keys())
    for i, u in enumerate(client_ids):
        dict_users[i] = dataset.dict_users[u]
    return dict_users, len(client_ids)

